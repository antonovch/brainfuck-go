package brainfuck

import "testing"

func TestInterpret(t *testing.T) {
	type args struct {
		program string
	}
	tests := []struct {
		name string
		args args
	}{
		{"hello world", args{"++++++++[>++++[>++>+++>+++>+<<<<-]>+>+>->>+[<]<-]>>.>---.+++++++..+++.>>.<-.<.+++.------.--------.>>+.>++."}},
		{"print 0 to 99", args{"++++++++++\n>++++++++++++++++++++++++++++++++++++++++++++++++\n>++++++++++\n[\n  >++++++++++++++++++++++++++++++++++++++++++++++++\n  >++++++++++\n  [\n    <<<. >>.+ <<<.\n    >>>>-\n  ]\n  <----------------------------------------------------------\n  <<+\n  >\n  -\n]"}},
		{"print 0 to 999", args{"++++++++++\n>++++++++++++++++++++++++++++++++++++++++++++++++\n>++++++++++\n[\n  >++++++++++++++++++++++++++++++++++++++++++++++++\n  >++++++++++\n  [\n\t  >++++++++++++++++++++++++++++++++++++++++++++++++\n\t  >++++++++++\n\t  [\n\t    <<<<<.\n\t    >>.\n\t    >>.+\n\t    <<<<<.\n\t    >>>>>>-\n\t  ]\n\t  <----------------------------------------------------------\n\t  <<+\n\t  >-\n  ]\n  <----------------------------------------------------------\n  <<+\n  >\n  -\n]"}},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			Interpret(tt.args.program)
		})
	}
}
